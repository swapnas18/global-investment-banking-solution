package loop;

public class factorial {

	public static void main(String[] args) {
		System.out.println("factorial of 7");
		int n=7;
		int result=n;
	for(int a=1;a<=n;a++) {
	result=result*a;
	System.out.println(result);
		
	}
	
	int  num=5;
	int total=num;
	for (int i=1;i<=num;i++) {
		total=total*i;
		System.out.println(total);
	}

}
}